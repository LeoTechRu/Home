import tkinter as tk
import random

definitions = {
    "While": "Цикл 'while' используется для выполнения блока кода, пока условие истинно.",
    "For": "Цикл 'for' используется для итерации по элементам последовательности.",
    "If": "Условие 'if' позволяет выполнить определенный блок кода, если условие истинно.",
    "Function": "Функция - это блок кода, который можно вызывать с определенными аргументами.",
    "List": "Список - это упорядоченная коллекция элементов, которая может содержать разные типы данных."
}

def show_random_definition():
    random_definition = random.choice(list(definitions.values()))
    definition_text.delete("1.0", tk.END)
    definition_text.insert(tk.END, random_definition)

root = tk.Tk()
root.title("Определения Python")
root.configure(bg="#00FFFF")

title_label = tk.Label(root, text="Определения Python", font=("Arial", 18), bg="#00FFFF")
title_label.pack(pady=10)

definition_text = tk.Text(root, height=5, width=40, font=("Arial", 12), wrap=tk.WORD)
definition_text.pack(pady=10)

show_button = tk.Button(root, text="Показать определение", command=show_random_definition, font=("Arial", 12), bg="#00FF00")
show_button.pack(pady=10)

root.mainloop()