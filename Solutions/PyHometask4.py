def proverka_vozrasta(age):  # тут создаём и обзываем функцию с аргументом возраста который зададим при вызове функции
    if age.isdigit() and int(age) >= 18:  # Проверяем, что введены цифры и возраст больше или равен 18
        return 'Да ты уже совсем большой стал, детскими сказками тебя уже не развлечь'  # обращаемся к юзеру
    elif age.isdigit() and 0 < int(age) < 18:  # Проверяем, что возраст старше 0 и меньше 18
        return 'А где твои родители, малыш?'  # Уточняем, где родители ребёнка
    else:  # В прочих случаях, если введены не числа
        return 'Не городи чепуху, малец. Тебя просили ввести возраст числом'  # Ругаем юзера за то, что вводит чепуху


'''Ну а далее мы вызовем функцию внутри вывода print и передадим в неё аргумент от пользователя'''
