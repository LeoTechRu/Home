def sravnivaem(a, b):  # Тут функция для второй части ДЗ, аргументы возьмём при обращении к ней
    if not a.isdigit() or not b.isdigit():  # Мне надоело прописывать каждому условию проверку на числа
        return 'Ты вновь пытаешься нас надурить, малец. Нужно было ввести только числа!'
    elif a == b:  # если равны
        return 'Оба числа равны'
    elif a > b:  # если первое больше
        return 'Первое число больше второго'
    elif a < b:  # если второе больше
        return 'Второе число больше первого'
    else:  # на случай сбоя в Матрице
        return 'Срочно вызывайте агента Смита, у нас сбой в Матрице!'


print(sravnivaem(input('Введи первое число: '),
                 input('Введи второе число: ')))  # делаем вывод функции и подаём аргументы от юзера


